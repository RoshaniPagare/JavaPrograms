package Sept.Ex_13092024_NonPrimitiveDataTypes;

public class Lab015_DiffBetweenPrintln_vs_Print {
    public static void main(String[] args) {
   // Difference Between println & print
        //println Terminates the current line and print statements on new line
        System.out.println("Hi");
        System.out.println("I am Roshani Pagare");
    // print doesn't terminates the current line continues to print statement on same line
        System.out.print("Hi");
        System.out.print("I am Roshani Pagare");
    }


}
